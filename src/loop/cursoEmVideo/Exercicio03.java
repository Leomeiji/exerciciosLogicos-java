package loop.cursoEmVideo;

public class Exercicio03 {

    public static void main(String[] args) {
        int contagem = 0;

        while (contagem <= 6) {
            int resultado = 3 * contagem;
            System.out.print(resultado + " ");
            contagem++;
        }

        System.out.print("Acabou!");
    }


}
